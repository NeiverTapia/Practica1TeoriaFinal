    private void JButtonEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonEvaluarActionPerformed
        // TODO add your handling code here:
        Automata1 automata = new Automata1(cont, enterChar, false);

        if(!JTFCadena.getText().isEmpty()){
             cadena = JTFCadena.getText().toUpperCase();
            automata.enterChar= cadena.toCharArray();
            automata.inicio();
        }
        if(automata.cad){
            JOptionPane.showMessageDialog(null, "La cadena ingresada es aceptada");
        }else{
            JOptionPane.showMessageDialog(null, "La cadena ingresada no es aceptada");
        }
        
    }//GEN-LAST:event_JButtonEvaluarActionPerformed

